-- 显示插入结果统计
SELECT 
    '地区配置数据插入完成' as message,
    COUNT(*) as total_records,
    COUNT(DISTINCT country) as unique_countries,
    COUNT(DISTINCT region) as unique_regions,
    COUNT(DISTINCT language_code) as unique_languages
FROM region_configs;

-- 按国家分组显示统计
SELECT 
    country as 国家,
    COUNT(*) as 语言数量,
    GROUP_CONCAT(language_name ORDER BY sort_order SEPARATOR ', ') as 支持语言
FROM region_configs 
WHERE is_active = 1
GROUP BY country
ORDER BY MIN(sort_order);